    private void VerificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VerificarMouseClicked

    //comentario agregado    
    if (Usuario_data.getText().isEmpty() || Usuario_pass.getText().isEmpty() ){ // tenemos que controlar que ambos TB tengan datos, si alguno est√° vacio, no puede ingresar
        JOptionPane.showMessageDialog(null, "Complete la informaci√≥n solicitada...","Entrada Inv√°lida",JOptionPane.WARNING_MESSAGE);
    }else{        
        //Si complet√≥ la informaci√≥n, la corrobora para ver si coincide con los autorizados
         String usuario="alumno@ulp.edu.ar";
         String pass="12345678";
         if (Usuario_data.getText().equalsIgnoreCase(usuario)){

             //El Usuario es el autorizado, ahora controlamos la password
            if (Usuario_pass.getText().equalsIgnoreCase(pass)){
               //El Usuario es el autorizado, la password es la indicada
               JOptionPane.showMessageDialog(null, "AUTORIZADO..","Bienvenido!",JOptionPane.INFORMATION_MESSAGE);
               dispose(); 
            }else{
               JOptionPane.showMessageDialog(null, "Acceso NO AUTORIZADO","Verificaci√≥n de Acceso al Sistema",JOptionPane.WARNING_MESSAGE);
            }

         }else{
            
             JOptionPane.showMessageDialog(null, "Acceso NO AUTORIZADO","Verificaci√≥n de Acceso al Sistema",JOptionPane.WARNING_MESSAGE);

         }
    
    }    

        
    }//GEN-LAST:event_VerificarMouseClicked

